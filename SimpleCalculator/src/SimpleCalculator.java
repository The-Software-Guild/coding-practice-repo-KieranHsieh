public class SimpleCalculator {
    public double add(double lhs, double rhs) {
        return lhs + rhs;
    }
    public double subtract(double lhs, double rhs) {
        return lhs - rhs;
    }
    public double multiply(double lhs, double rhs) {
        return lhs * rhs;
    }
    public double divide(double lhs, double rhs) {
        return lhs / rhs;
    }
}
